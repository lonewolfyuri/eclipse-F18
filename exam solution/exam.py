from collections import defaultdict

# You might use these abbreviations in your code
# c  : caller
# r  : receiver
# t  : time
# ts : list of times


def all_parties(db : {str:{str:[int]}}) -> {str}:
    return set(db.keys()).union({r for r_ts in db.values() for r in r_ts.keys()})
    # Or
    answer = set()
    for c,rs_ts in db.items():
        answer.add(c)
        for r in rs_ts.keys():
            answer.add(r)
    return answer



def read_db(file : open) -> {str:{str:[int]}}:
    calls = defaultdict(lambda : defaultdict(list))
    for line in file:
        c,r,*ts = line.rstrip().split(':')
        calls[c][r] += [int(t) for t in ts]
    return {r:{k:v for k,v in rest.items()} for r,rest in calls.items()}    



def talker_frequency (db : {str:{str:[int]}}) -> [(str,int)]:
    answer = defaultdict(int)
    for c,rs_ts in db.items():
        for r,ts in rs_ts.items():
            answer[c] += len(ts)
            answer[r] += len(ts)
    return sorted(answer.items(), key=lambda x : (-x[1],x[0]))



def mutual_callers (db : {str:{str:[int]}}) -> {(str,str)}:
    answer = set()
    for c,rs_ts in db.items():
        for r,ts in rs_ts.items():
            if r in db and c in db[r].keys():
                answer.add(tuple(sorted((c,r))))
    return answer  



def initiators (db : {str:{str:[int]}}) -> [str]:
    answer = defaultdict(lambda : [0,0])
    for c,rs_ts in db.items():
        for r,ts in rs_ts.items():
            answer[c][0] += len(ts)
            answer[r][1] += len(ts)
    return [x[0] for x in sorted(answer.items(), key=lambda x : (-x[1][0],x[1][1],x[0]))]
    # Or
    return sorted(answer.keys(), key=lambda x : (-answer[x][0],answer[x][1],x))


def matrix (db : {str:{str:[int]}}) -> [[int]]:
    names = sorted (all_parties(db))
    answer = [len(names)*[0] for _e in range(len(names))]
    for r in names:
        ri = names.index(r)
        for e in names:
            ei = names.index(e)
            if r in db and e in db[r]:
                answer[ri][ei] += sum(db[r][e])
                answer[ei][ri] += sum(db[r][e])
    return answer    





